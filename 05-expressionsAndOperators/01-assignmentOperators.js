// assignment Operators

let x= 5;

//Addition Assignment
x += 1; // x= x + 1

//subtraction Assignment
x -= 1; // x = x - 1

//multiplication assignment
x *= 1; // x = x * 1

// division assignment
x /= 1; // x = x / 1

// reainder (or modulus) assignment
x %= 1 // x = x % 1

// exponential assignment
 x **=1 // x = x ** 1